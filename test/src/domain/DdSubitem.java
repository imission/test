package domain;

public class DdSubitem {

	private int id;
	private String item_num;
	private String sub_num;
	private String sub_name;
	private String unit;
	private String ref;
	private String stype;
	private String value_type;
	private String in_template;
	private String out_template;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getItem_num() {
		return item_num;
	}
	public void setItem_num(String item_num) {
		this.item_num = item_num;
	}
	public String getSub_num() {
		return sub_num;
	}
	public void setSub_num(String sub_num) {
		this.sub_num = sub_num;
	}
	public String getSub_name() {
		return sub_name;
	}
	public void setSub_name(String sub_name) {
		this.sub_name = sub_name;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getRef() {
		return ref;
	}
	public void setRef(String ref) {
		this.ref = ref;
	}
	public String getStype() {
		return stype;
	}
	public void setStype(String stype) {
		this.stype = stype;
	}
	public String getValue_type() {
		return value_type;
	}
	public void setValue_type(String value_type) {
		this.value_type = value_type;
	}
	public String getIn_template() {
		return in_template;
	}
	public void setIn_template(String in_template) {
		this.in_template = in_template;
	}
	public String getOut_template() {
		return out_template;
	}
	public void setOut_template(String out_template) {
		this.out_template = out_template;
	}
	
}
